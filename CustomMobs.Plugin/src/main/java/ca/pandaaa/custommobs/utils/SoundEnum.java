    SoundEnum(String key, String materialName, String category) {
        this.key = key;
        this.material = getMaterialIfAvailable(materialName);
        this.category = category;
    }

    private static Material getMaterialIfAvailable(String materialName) {
        try {
            return Material.valueOf(materialName);
        } catch (IllegalArgumentException e) {
            return null;
        }
    }

    public static List<SoundEnum> getSoundsByCategory(String category) {
        List<SoundEnum> sounds = new ArrayList<>();
        for(SoundEnum sound : SoundEnum.values()) {
            if (sound.material == null)
                continue;
            if(sound.category.equalsIgnoreCase(category))
                sounds.add(sound);
        }
        return sounds;
    }

    public static List<String> getSoundKeys() {
        List<String> soundKeys = new ArrayList<>();
        for(SoundEnum sound : SoundEnum.values()) {
            soundKeys.add(sound.key);
        }
        return soundKeys;
    }

