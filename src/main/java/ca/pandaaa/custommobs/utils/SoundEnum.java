    private final Material material;
    private final String category;

    SoundEnum(String materialName, String category) {
        this.material = getMaterialIfAvailable(materialName);
        this.category = category;
    }

    private static Material getMaterialIfAvailable(String materialName) {
        try {
            return Material.valueOf(materialName);
        } catch (IllegalArgumentException e) {
            return null;
        }
    }

    public static List<SoundEnum> getSoundsByCategory(String category) {
        List<SoundEnum> sounds = new ArrayList<>();
        for(SoundEnum sound : SoundEnum.values()) {
            if (sound.material == null)
                continue;
            if(sound.category.equalsIgnoreCase(category))
                sounds.add(sound);
        }
        return sounds;
    }

    public static List<String> getSoundsName() {
        List<String> soundsName = new ArrayList<>();
        for(SoundEnum sound : SoundEnum.values()) {
            soundsName.add(sound.name());
        }
        return soundsName;
    }

    public Material getMaterial() {
        return material;
    }

    public Sound getSound() {
        try {
            return Sound.valueOf(name());
        } catch (IllegalArgumentException e) {
            System.out.println("No matching Sound for: " + name());
            return null;
        }
    }
